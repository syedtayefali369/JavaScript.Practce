let firstName = "Tayef";
let lastName = "Ali";
let fullName = firstName + " " + lastName;
console.log(fullName);
// explaination:
// 1. firstName is a string
// 2. lastName is a string
// 3. fullName is a string
// 4. specifically using the + operator, the += operator, and the concat() method ,we concate 2 strings
// 5. fullName is the result of concatenating firstName and lastName with a space in between using the + operator
// 6. fullName is stored in the variable fullName
// 7. fullName is then logged to the console
// 8. fullName is returned by the console.log() function
